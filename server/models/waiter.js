const mongoose = require('mongoose');

const waiterSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        trim: true,
    },
    phone: {
        type: String,
        required: true,
        trim: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
});

const Waiter = mongoose.model('Waiter', waiterSchema);

module.exports = Waiter;